public class TimeComponents {


    private long Day;
    public final long getDay()
    {
        return Day;
    }
    public final void setDay(long value)
    {
        Day = value;
    }

    private long Hour;
    public final long getHour()
    {
        return Hour;
    }
    public final void setHour(long value)
    {
        Hour = value;
    }

    private long Minute;
    public final long getMinute()
    {
        return Minute;
    }
    public final void setMinute(long value)
    {
        Minute = value;
    }

    private long Second;
    public final long getSecond()
    {
        return Second;
    }
    public final void setSecond(long value)
    {
        Second = value;
    }

}
